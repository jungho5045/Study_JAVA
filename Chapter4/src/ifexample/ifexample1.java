package ifexample;

public class ifexample1 {

	public static void main(String[] args) {
		
		char gender = 'M';
		
		if( gender == 'F' ) {
			System.out.println("여성입니다.");		// 수행문1
		}else {
			System.out.println("여성이 아닙니다.");	// 수행문2
		}
		
		/*
		 * if문, if-else문
		 * 조건식의 결과에 따라 수행문이 실행되는 조건문
		 * if(조건식){
		 * 		수행문;
		 * }
		 * 
		 * if(조건식){
		 * 		수행문1;
		 * }else{
		 * 		수행문2;
		 * }
		 */
	}

}
