package operator;

public class OperatorEx6 {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 20;
		int max = (num1 > num2) ? num1 : num2;
		
		System.out.println(max);
		
		
		/*
		 * 조건 연산자
		 * 삼항 연산자
		 * 조건식 ? 결과1 : 결과2;
		 * int num = (5>3)? 10 : 20;
		 * 간단한 조건문 대신 사용할 수 있는 연산자
		 * 조건식이 true면 결과1을, false면 결과2를 결과값으로 가진다.
		 */
	}

}
